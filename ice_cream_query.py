
want_ice_cream = input("Do you want ice cream? (yes/no): ")

if want_ice_cream.lower() == "yes":
    print("Great! Enjoy your ice cream!")
elif want_ice_cream.lower() == "no":
    print("No problem. Maybe next time!")
else:
    print("I'm sorry, I didn't understand your response. Please answer with 'yes' or 'no'.")
